    private void ThemKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemKHActionPerformed
        // TODO add your handling code here:
        try {
            String maKh = txtMaKH.getText().trim();
            String tenKh = txtTenKH.getText().trim();
            String sdtKh = txtsodienthoai.getText().trim(); 
            String cmtKH = txtcmtKH.getText().trim();
            String ngaySinhKH = txtngaysinh.getText().trim();
                String diachiKh = txtDiaChiKH.getText().trim();
          boolean gioiTinhKH = (cbxgioitinh.getSelectedIndex() == 1);

            if (maKh.equals("") || tenKh.equals("") || sdtKh.equals("") || diachiKh.equals("") || cmtKH.equals("") ) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            } else {
                KhachHang kh = new KhachHang();
                kh.setMaKhachHang(maKh);
                kh.setTenKhachHang(tenKh);
                kh.setSoDienThoai(sdtKh);
                kh.setDiaChi(diachiKh);  
                kh.setNgaySinh(ngaySinhKH);
                kh.setChungMinhThu(cmtKH);
       
                // Xác định giới tính dựa trên giá trị nhập vào
               
                kh.setGioiTinh(gioiTinhKH);

                KhachHangDAO.getInstance().insert(kh);
                JOptionPane.showMessageDialog(this, "Thêm thành công !");
                parent.loadDataToTable(KhachHangDAO.getInstance().selectAll());
                this.dispose();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập giới tính là số (0 hoặc 1) !", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            System.out.println("view.AddKhachHang.ThemKHActionPerformed()");
            JOptionPane.showMessageDialog(this, "Thêm không thành công !", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ThemKHActionPerformed

