public String createIdPC() {
        ArrayList<KhachHang> mtAll = KhachHangDAO.getInstance().selectAll();

        int i = 1;
        while (true) {
            boolean exists = false;
            for (KhachHang khachhang : mtAll) {
                if (khachhang.getMaKhachHang().equals("KH" + i)) {
                    exists = true;
                    break;
                }
            }
            if (!exists) {
                break;
            } else {
                i++;
            }
        }

        return "KH" + i;

    }
