    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        // Lấy thông tin từ các trường nhập liệu
        String maDichVu = txtMaDichVu.getText();
        String tenDichVu = txtTenDichVu.getText();
        int soLuong = 0;
        double donGia = 0;

        try {
            // Ép kiểu và lấy giá trị từ các trường số lượng và đơn giá
            soLuong = Integer.parseInt(txtSoLuong.getText());
            donGia = Double.parseDouble(txtDonGia.getText());
        } catch (NumberFormatException e) {
            // Xử lý nếu nhập liệu không hợp lệ
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng và đơn giá ở dạng số !");
            return; // Dừng phương thức khi có lỗi
        }

        // Kiểm tra nếu mã sản phẩm hoặc tên sản phẩm rỗng
        if (maDichVu.isEmpty() || tenDichVu.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !");
            return; // Dừng phương thức khi có lỗi
        } else {
            // Tạo đối tượng DichVu với thông tin đã nhập
            DichVu dv = new DichVu(maDichVu, tenDichVu, soLuong, donGia);
            // Gọi phương thức insert từ lớp DichVuDAO để thêm sản phẩm vào cơ sở dữ liệu
            DichVuDAO.getInstance().insert(dv);
            // Đóng cửa sổ hiện tại sau khi thêm sản phẩm thành công
            this.dispose();
            // Hiển thị thông báo thành công
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công !");
            // Tải lại dữ liệu vào bảng sau khi thêm sản phẩm
            owner.loadDataToTable();
        }

    }//GEN-LAST:event_btnAddProductActionPerformed

