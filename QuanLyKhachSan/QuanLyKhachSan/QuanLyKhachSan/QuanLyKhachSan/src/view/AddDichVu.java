    /**
     * @param args the command line arguments
     */
    public String createIdPC() {
        ArrayList<DichVu> mtAll = DichVuDAO.getInstance().selectAll();

        int i = 1;
        while (true) {
            boolean exists = false;
            for (DichVu mayTinh : mtAll) {
                if (mayTinh.getMaDichVu().equals("DV" + i)) {
                    exists = true;
                    break;
                }
            }
            if (!exists) {
                break;
            } else {
                i++;
            }
        }

        return "DV" + i;

    }

